/* ------------------------------------------------------------------ */
/* Montgomery reduction                                                 */
/* ------------------------------------------------------------------ */

int32_t pqc_mldsa_montgomery_reduce(int64_t a)
{
    int32_t t;

    t = (int32_t)((int64_t)(int32_t)a * (int64_t)PQC_MLDSA_QINV);
    t = (int32_t)((a - (int64_t)t * PQC_MLDSA_Q) >> 32);
    return t;
}

/* ------------------------------------------------------------------ */
/* Barrett reduction                                                    */
/* ------------------------------------------------------------------ */

int32_t pqc_mldsa_barrett_reduce(int32_t a)
{
    int32_t t;
    const int32_t v = ((1LL << 26) + PQC_MLDSA_Q / 2) / PQC_MLDSA_Q;

    t = (int32_t)((int64_t)v * a + (1 << 25)) >> 26;
    t *= PQC_MLDSA_Q;
    return a - t;
}

/* ------------------------------------------------------------------ */
/* Forward NTT                                                          */
/* ------------------------------------------------------------------ */

void pqc_mldsa_ntt(int32_t a[256])
{
    unsigned len, start, j, k_idx;
    int32_t zeta, t;

    k_idx = 0;
    for (len = 128; len >= 1; len >>= 1) {
        for (start = 0; start < 256; start = j + len) {
            k_idx++;
            zeta = zetas[k_idx];
            for (j = start; j < start + len; j++) {
                t = pqc_mldsa_montgomery_reduce((int64_t)zeta * a[j + len]);
                a[j + len] = a[j] - t;
                a[j] = a[j] + t;
            }
        }
    }
}

/* ------------------------------------------------------------------ */
/* Inverse NTT                                                          */
/* ------------------------------------------------------------------ */

void pqc_mldsa_invntt(int32_t a[256])
{
    unsigned start, len, j, k_idx;
    int32_t t, zeta;
    /* f = Mont^{-2} / 256 = 41978 in Montgomery form */
    const int32_t f = 41978;

    k_idx = 256;
    for (len = 1; len <= 128; len <<= 1) {
        for (start = 0; start < 256; start = j + len) {
            k_idx--;
            zeta = -zetas[k_idx];
            for (j = start; j < start + len; j++) {
                t = a[j];
                a[j] = t + a[j + len];
                a[j + len] = t - a[j + len];
                a[j + len] = pqc_mldsa_montgomery_reduce(
                    (int64_t)zeta * a[j + len]);
            }
        }
    }

    for (j = 0; j < 256; j++) {
        a[j] = pqc_mldsa_montgomery_reduce((int64_t)f * a[j]);
    }
}

/* ------------------------------------------------------------------ */
/* Pointwise Montgomery multiplication                                  */
/* ------------------------------------------------------------------ */

void pqc_mldsa_poly_pointwise_montgomery(int32_t c[256],
                                          const int32_t a[256],
                                          const int32_t b[256])
{
    unsigned i;
    for (i = 0; i < PQC_MLDSA_N; i++) {
        c[i] = pqc_mldsa_montgomery_reduce((int64_t)a[i] * b[i]);
    }
}
